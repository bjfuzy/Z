    public String submitPostData(Map<String,String> params,String encode)
    {
        byte[] data = getRequestData(params, encode).toString().getBytes();//获得请求体
        String COOKIES_HEADER = "Set-Cookie";

        try
        {
            URL url = new URL("http://159.226.179.48:8080/zy/Test");
            //URL url = new URL("http://159.226.179.182:8080/zy/Test");
            HttpURLConnection httpURLConnection = (HttpURLConnection)url.openConnection();
            httpURLConnection.setConnectTimeout(3000);              //设置连接超时时间
            httpURLConnection.setDoInput(true);                  //打开输入流，以便从服务器获取数据
            httpURLConnection.setDoOutput(true);                 //打开输出流，以便向服务器提交数据
            httpURLConnection.setRequestMethod("POST");         //设置以Post方式提交数据
            httpURLConnection.setUseCaches(false);               //使用Post方式不能使用缓存
            //设置请求体的类型是文本类型
            httpURLConnection.setRequestProperty("Content-Type", "application/x-www-form-urlencoded");
            //设置请求体的长度
            httpURLConnection.setRequestProperty("Content-Length", String.valueOf(data.length));

            //httpURLConnection.setRequestProperty("Cookie", sessionId);
            //获得输出流，向服务器写入数据
            OutputStream outputStream = httpURLConnection.getOutputStream();
            outputStream.write(data);

            int response = httpURLConnection.getResponseCode();            //获得服务器的响应码

            Log.v("response", Integer.toString(response));

            if(response == HttpURLConnection.HTTP_OK) {
                //第一次连接成功后，服务器会把sessionID包装在cookieHeader里面，下面是把sessionID提出来的过程

                //Log.v("sharedsave",pref.getString("sessionid",""));

                //===============================提取sessionID===============================================
                Map<String, List<String>> headerFields = httpURLConnection.getHeaderFields();
                List<String> cookiesHeader = headerFields.get(COOKIES_HEADER);
                if(cookiesHeader != null)
                {
                    for (String cookie : cookiesHeader)
                    {
                        mCookieManager.getCookieStore().add(null, HttpCookie.parse(cookie).get(0));
                    }
                }
                Log.v("cookies", TextUtils.join(";", mCookieManager.getCookieStore().getCookies()));
                mEditor = mPreferences.edit();

                mEditor.putString("sessionid", TextUtils.join(";", mCookieManager.getCookieStore().getCookies()));
                mEditor.commit();
                //=============================提取完毕======================================================

                InputStream inputStream = httpURLConnection.getInputStream();
                return dealResponseResult(inputStream);                     //处理服务器的响应结果

            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return "";
    }

    //=================================两个函数都是为了对服务端给的数据进行解析======================================
    public static StringBuffer getRequestData(Map<String, String> params, String encode)
    {
        StringBuffer stringBuffer = new StringBuffer();        //存储封装好的请求体信息
        try
        {
            for(Map.Entry<String, String> entry : params.entrySet())
            {
                stringBuffer.append(entry.getKey())
                        .append("=")
                        .append(URLEncoder.encode(entry.getValue(), encode))
                        .append("&");
            }
            stringBuffer.deleteCharAt(stringBuffer.length() - 1);    //删除最后的一个"&"
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        return stringBuffer;
    }

    public static String dealResponseResult(InputStream inputStream) {
        String resultData = null;      //存储处理结果
        ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
        byte[] data = new byte[1024];
        int len = 0;
        try {
            while((len = inputStream.read(data)) != -1) {
                byteArrayOutputStream.write(data, 0, len);
            }
        }
        catch (IOException e)
        {
            e.printStackTrace();
        }
        resultData = new String(byteArrayOutputStream.toByteArray());
        Log.v("resultData",resultData);
        return resultData;
    }
